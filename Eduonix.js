/*The below is a switch statement. THis will cycle through the cases until it finds one that evaluates as true or it will return the default case.
Switch statements are used to replace complicated nests of for loops
This switch will create an alert to say "It's Tuesday"*/


let day = 2;

switch (day) {
    case 0:
        alert("It's sunday");
        break;
    case 1:
        alert("It's Monday");
        break;
    case 2: 
        alert("It's Tuesday");
        break;
    default:
        alert("I aint got a clue bro");
        break;
};



